package Lession5;

public class Bai4_Product {
	private String name;
	private long price;
	
	public Bai4_Product() {
	}

	public Bai4_Product(String name, long price) {
		this.name = name;
		this.price = price;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public long getPrice() {
		return price;
	}

	public void setPrice(long price) {
		this.price = price;
	}

}
